def check_score(matrix):
    result_value = 0
    symbol_values = {"#": 5, "O": 3, "X": 1, "!": -1, "!!": -3, "!!!": -5}
    for i in range(len(matrix)):
        for j in range(len(matrix[i])):
            result_value += symbol_values[matrix[i][j]]
    return (result_value > 0) * result_value


